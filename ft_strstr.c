#include "libft.h"

char	*ft_strstr(const char *s1, const char *s2)
{
	int i;

	if (!*s1 && !*s2)
		return ((char *)s1);
	while (*s1 != '\0')
	{
		i = 0;
		while (s1[i] && s1[i] == s2[i])
			i += 1;
		if (!s2[i])
			return ((char *)s1);
		++s1;
	}
	return (NULL);
}
